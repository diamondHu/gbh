    <script type="text/javascript">


        function scrollTo(ele, speed){
            if(!speed) speed = 300;
            if(!ele){
                $("html,body").animate({scrollTop:0},speed);
            }else{

                if(ele.length>0) $("html,body").animate({scrollTop:$(ele).offset().top-250},speed);
            }
            return false;
        }


        $(document).ready(function(){
            $('.ui.sticky')
                    .sticky({
                        context: '#recruitBoxList',
                        pushing: true,
                        onStick : function() {

                            $(this).addClass('recruit-title-list-scroll');
                        },

                        onUnstick : function() {
                            $(this).removeClass('recruit-title-list-scroll');
                        }
                    })
            ;

            $('#gadBtn').click(function(){
                scrollTo('#gad',400);
            })
            $('#uiBtn').click(function(){
                scrollTo('#ui',400);
            })
            $('#phpBtn').click(function(){
                scrollTo('#php',400);
            })
            $('#ooBtn').click(function(){
                scrollTo('#oo',400);
            })
        })
    </script>

